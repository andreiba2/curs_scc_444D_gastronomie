def preparare():
    return '''
        <ol>
            <li>Topește ciocolata și untul pe baie de aburi, amestecând până obții o compoziție omogenă.</li>
            <li>Separat, bate ouăle cu zahărul până devin spumoase.</li>
            <li>Combină ciocolata răcită cu amestecul de ouă, apoi încorporează făina și sarea.</li>
            <li>Toarnă compoziția în forme unse cu unt și date cu cacao.</li>
            <li>Coace la 200°C timp de 10-12 minute — marginile trebuie să fie ferme, centrul moale.</li>
            <li>Lasă 1 minut să se răcească, răstoarnă pe farfurie și servește imediat.</li>
        </ol>
        <a href="/lava-cake">Inapoi</a>
    '''

