def origine_macarons():
    return "Deși sunt asociate cu Franța, primele macarons au apărut în Italia, fiind aduse în Franța de bucătarul Caterinei de Medici în secolul al XVI-lea."

def ingrediente_macarons():
    return "Făină de migdale, zahăr pudră, albușuri de ou, zahăr tos și diverse umpluturi (ganache, gem, cremă de unt)."

def preparare_macarons():
    return "Se obține o bezea fermă, se încorporează ușor făina de migdale (macaronage), se poșează în cercuri, se lasă la uscat pentru a face o crustă și se coc la temperatură precisă."
