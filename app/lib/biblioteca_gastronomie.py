def origine_piftie():
    return """Piftia (sau răcitura) este un preparat tradițional românesc, adânc înrădăcinat în gastronomia Europei de Est și a Balcanilor, pregătit în mod tradițional pentru sărbătorile de iarnă (Crăciun și Bobotează)."""

def ingrediente_piftie():
    return """Picioare, urechi și rasoale de porc (pentru gelatină), carne macră, mult usturoi, morcovi, sare, boabe de piper negru și frunze de dafin."""

def preparare_piftie():
    return """Carnea se fierbe la foc foarte mic, timp de câteva ore, îndepărtând constant spuma. Spre final se adaugă legumele și condimentele. După ce carnea se desprinde ușor de pe oase, zeama se strecoară, se amestecă cu usturoi pisat, apoi se toarnă peste carnea așezată în boluri și se lasă la rece până se încheagă (gelatinizează)."""
