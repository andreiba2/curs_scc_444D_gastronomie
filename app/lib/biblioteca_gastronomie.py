def provenienta_chilli_con_carne():
    return ("Chilli con carne este un preparat originar din regiunea Tex-Mex "
            "(zona de graniță dintre Texas, Statele Unite, și nordul Mexicului). "
            "Este recunoscut ca felul de mâncare oficial al statului Texas din anul 1977.")

def ingrediente_chilli_con_carne():
    return ("Carne de vită tocată, fasole roșie, roșii cuburi, "
            "ceapă, usturoi, ardei iute, boia afumată, chimion, "
            "pastă de roșii, sare, piper și ulei de măsline.")

def preparare_chilli_con_carne():
    return ("Se călește ceapa cu usturoiul, se adaugă carnea tocată și se rumenește. "
            "Se adaugă condimentele (boia, chimion, ardei iute), apoi roșiile cuburi "
            "și pasta de roșii. Se fierbe la foc mic 30-45 de minute, iar la final "
            "se adaugă fasolea roșie și se mai fierbe 10 minute. Se servește cald, "
            "alături de orez sau lipie.")
